/* prompt("100 이하의 첫 번째 숫자를 입력하세요"):

  사용자가 첫 번째 숫자를 입력하도록 요청합니다.
  입력된 값은 문자열로 저장됩니다.

if (isNaN(A) || A > 100 || A === ""):

  isNaN(A):
  - A가 숫자가 아닌 경우(NaN일 경우) true가 됩니다.
  A > 100:
  - A가 100보다 큰 경우 true가 됩니다.
  A === "":
  - 사용자가 아무것도 입력하지 않고 그냥 확인 버튼을 눌렀을 경우 빈 문자열("")이 들어가므로, 이 조건을 통해 빈 값이 들어갔을 때 true가 됩니다.

만약 A가 숫자가 아니거나 100을 초과하거나 빈 값이 입력되면, alert()로 경고 메시지를 띄우고 window.location.reload()로 페이지를 새로 고칩니다. 즉, 페이지가 새로 고쳐지면서 다시 첫 번째 숫자를 입력받습니다.
 */

// parseInt = 는 사용자가 입력한 문자열을 정수로 변환합니다. 예를 들어, 사용자가 "50"을 입력하면 50으로 변환됩니다.

// while = 숫자가 아니거나, 100을 초과하거나, 빈 값일 경우에는 잘못된 값이 들어갔다는 것이므로 반복문을 통해 계속해서 입력을 받습니다.
// while 문은 조건이 참일 동안 계속해서 반복되는 제어문입니다. 즉, 특정 조건이 true일 때 계속해서 같은 코드를 반복 실행합니다. 이때 조건이 false가 되면 반복이 종료됩니다.

let A = prompt("100 이하의 첫 번째 숫자를 입력하세요");
if (isNaN(A) || A > 100 || A === "") {
  alert("숫자만 입력해 주세요. 100 이하의 숫자만 입력해주세요.");
  window.location.reload(); // 페이지 새로고침
} else {
  A = parseInt(A);

  let B = prompt("100 이하의 두 번째 숫자를 입력하세요");
  while (isNaN(B) || B > 100 || B === "") {
    // 잘못된 값일 경우 반복
    alert("숫자만 입력해 주세요. 100 이하의 숫자만 입력해주세요.");
    B = prompt("100 이하의 두 번째 숫자를 입력하세요");
  }
  B = parseInt(B);

  let C = prompt("100 이하의 세 번째 숫자를 입력하세요");
  while (isNaN(C) || C > 100 || C === "") {
    // 잘못된 값일 경우 반복
    alert("숫자만 입력해 주세요. 100 이하의 숫자만 입력해주세요.");
    C = prompt("100 이하의 세 번째 숫자를 입력하세요");
  }
  C = parseInt(C);

  // Math.min = 값 중 제일 작은 값 결과 도출
  let smallest = Math.min(A, B, C);

  console.log(`가장 작은 값은 ${smallest} 입니다.`);
}

